                <h4>Kdo je David?</h4>
                    <p>David v Bibliji je nov izbranec za kralja Izraela. Kot kralj je pomembna figura v krščanstvu, judovstvu in islamu. V Prvi Samuelovi knjigi se prvič pojavi v 16. poglavju. V nadaljnjih poglavjih Prve Samuelove knjige, v celotni Drugi Samuelovi knjigi in v prvih dveh poglavjih Prve Knjige kraljev je opisano njegovo življenje (Rothbat 2). V Prvi Samuelovi knjigi se spoznamo z Davidom kot pastirjem in ga kasneje spremljamo na dvoru ob kralju Savlu. V Drugi Samuelovi knjigi je David predstavljen kot kralj, v prvih dveh poglavjih Prve Knjige kraljev pa nastopi njegova smrt. Njemu se pripisujeta ustanovitev judejske dinastije in združitev vseh rodov Izraela pod enim samim monarhom. Za njim je njegov sin Solomon še povečal imperij, ki ga je zgradil. Po Davidovi liniji naj bi prišel judovski Mesija in iz njega naj bi izhajal tudi Jezus Kristus (Kalimi in Hass 198).</p>
                    <p>V Firencah je bil David priljubljen predmet upodabljanja. Pred Michelangelom sta Davida v obliki kipa upodobila že Donatello in Andrea del Verrocchio. Za razliko od kasnejšega Michelangelovega dela, sta njuna kipa prikazovala Davida, ki je že premagal filisteškega velikana Goljata. Glava, ki jo v svetopisemski zgodbi David odseka Goljatu, namreč leži ob nogah njunih kipov (Altabe 32). V renesansi je ta prizor uprizoril tudi Andrea del Castagno, ki je naslikal mladeniča pri zamahu, čeprav Goljatova glava že leži med njegovimi nogami.</p>
                    <p>Kipa Donatella in Verrocchia predstavljata Davida, kot je opisan v Bibliji, to je kot navadnega pastirja, ki mu uspe premagati velikana, medtem ko Michelangelov David po zunanji podobi predstavlja junaka, kakršnega so umetniki upodabljali v antiki (Altabe 32).</p>
