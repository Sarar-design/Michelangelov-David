                    <h4>Freska v Sistinski kapeli</h4>
                    <p>Michelangelo je kasneje še enkrat upodobil kralja Davida. Naslikal je fresko v Sikstinski kapeli, ki prikazuje drugačen prizor. David je tu prikazan, kako zamahne z Goljatovim mečem, da bi mu odsekal glavo. Michelangelov zgodnejši kip bi lahko služil kot predloga za fresko, ki nadaljuje biblično zgodbo. Kip prikazuje Davida v trenutku pred spopadom, na freski pa je prikazan sredi dejanja dokončnega uboja, kateremu sledi Davidovo zmagoslavje, ki so ga radi upodabljali drugi umetniki (Rothbath 33).</p>
