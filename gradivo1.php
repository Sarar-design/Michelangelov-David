                <h4>Simbol Ferenc</h4>
                    <p>Ko je Michelangelo sprejel naročilo za upodabljanje biblične figure, je bilo mišljeno, da bo kip postavljen visoko na vrhu katedrale. V svoji končni obliki pa je požel tako občudovanje, da ga je mestni svet namesto tega dal postaviti na glavni mestni trg Palazzo della Signoria poleg mestne hiše, kjer je bil sedež vlade. Po letu 1494, ko so izgnali močno oligarhično družino Medici, se je v Firencah za kratek čas obnovila republikanska vlada (Stokstad in Cothern 644–645).</p>
                    <p>Zaradi postavitve na glavni mestni trg in zaradi neustrašne, mirne in samozavestne, drže, ki nakazuje, da Davidu uspelo premagati sebi močnejšega nasprotnika, je kip privzel političen pomen. Firence so se identificirale z Davidom oziroma s pripravo mladeniča, ki je bil popolnoma neizkušen v bojevanju, na spopad z filistejskim velikanom. Kip Davida je postal simbol mesta (Gravett 276–278).</p>
                    <p>Razen od družine Medici so bile Firence v tem času ogrožene tudi od zunanje nevarnosti z vseh strani, zato je kip Davida predstavljal moč in svobodo Firenc. Prikazoval je napet trenutek pred bojem namesto sproščenosti po njem, kot sta jo prikazovala prejšnja kipa Donatella in Verrocchia, ter zaupanje v lastne sposobnosti (Di Cagno 24).</p>
                    <p>Da je bil kip simbol mesta, omenja že Giorgio Vasari. Ko komentira njegov kip trdi: »Ta je bil mišljen kot simbol svobode za palačo, kar pomeni, da tako kot je David ščitil svoje ljudstvo, mora kdorkoli, ki vlada Firencam, odločno braniti mesto in mu pravično vladati« (Vasari v Kleiner 633–632).</p>
