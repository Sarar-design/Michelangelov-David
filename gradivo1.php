                <h4>Golost kipa</h4>
                    <p>Michelangelo je Davida naredil golega. Lahko bi si predstavljali, da je David šel v spopad gol, ko zavrne Savlova oblačila in vojaško opremo, ker je ni bil vajen in bi ga samo ovirala. Kot je to opisano v besedilu: Savel je potem Davida oblekel v svojo bojno opravo, mu dal bronasto čelado na glavo in ga oblekel v oklep. David si je čez njegovo bojno opravo opasal njegov meč in poskusil hoditi, ker ni bil vajen. David je rekel Savlu: »Ne morem hoditi v tem, ker nisem vajen.« In David je odložil to s sebe … (1 Sam 17, 38 39)</p>
                    <p>Ni podatka o tem, ali je zares gol ali ne, a vemo, da je brez kakršnekoli zaščite in s tem bolj ranljiv kot njegov nasprotnik. Lahko interpretiramo, da David s tem, ko zavrne oklep, izkaže samozavest in pogum.</p>
                    <p>Michelangelo je želel prikazati junaka. Golota kipa odraža starodavno prepričanje, da ima prikazana figura božanske lastnosti (Szpek 40). V antiki so pogosto prikazovali golost. V renesansi, v kateri je deloval Michelangelo, pa je bil ideal obnoviti klasične oblike v umetnosti in upodobiti junaštvo. Michelangelo se je pri prikazovanju svetopisemskega prizora očitno opiral na klasični antični goli kip. A kljub temu, da so se renesančni umetniki opirali na antiko, golota ni bila pogost element v njihovih umetniških delih. Michelangelov kip je bil prvi goli kip vse od antičnih časov, ki je bil postavljen na ogled v javnost. Ker kip ne nosi oblačil, je Michelangelo ustvaril junaka, ki nima elementov, ki bi spominjali na določen zgodovinski čas. Njegov junak nima historične identitete in je brezčasen. S tem je Michelangelo ujel »univerzalno« realnost junaštva (Leithart 74).</p>
