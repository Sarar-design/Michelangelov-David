                <h4>Drža kipa</h4>
                <p>Drža Michelangelovega kipa je povezana z umetnostjo v antiki. To je drža, ki je bila priljubljena med starimi Grki in se imenuje kontrapost (Leithart 74). Davidova desna noga nosi polno težo, leva pa je nekoliko pokrčena. Ta drža povzroči, da se boki in ramena zasučejo v nasprotnih kotih, kar daje celotnemu trupu rahlo S-krivuljo. Daje vizualno iluzijo, da je telo v gibanju. Po upodabljanju take drže je najbolj znan antični kipar Poliklet. To je drža tradicionalnega junaka, tako kot je tudi Davidovo mišičasto, zrelo telo značilno za takega junaka (Szpek 40). Pri Michelangelovem Davidu drža namiguje na akcijo, ki se bo kmalu zgodila.</p>
