                <h4>Roki in glava</h4>
                <p>Roki kipa in tudi njegova glava sta zelo veliki v primerjavi z drugimi deli telesa. S tem se gledalčeva pozornost osredotoči na to, kar glava in roki simbolizirata. To sta mišljenje in dejanje (The Canonical 103–104). Večji roki usmerjata pozornost na »orožje«, ki ga David drži in s katerim bo premagal Goljata (Fichner-Rathusstran 247). To nas torej napeljuje na interpretacijo, da prikazuje mladega Davida iz Biblije v trenutku, ko se pripravlja na spopad. K temu napeljujejo tudi vidno nabrekle žile in zategnjena tetiva, ki krepijo psihološko energijo Davidove drže (Kleiner 633-632).</p>
