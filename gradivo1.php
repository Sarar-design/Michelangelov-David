                <h4>Zgodba iz Biblije</h4>
                <p>David je v 1. Samuelovi knjigi pastir iz Betlehema, ki se znajde na dvoru v Izraelu, kjer kralju postane oproda. Ko so se Filistejci in Izraelci pripravljali na spopad, se je David spopadel z velikanom, Goljatom. Filistejci so se zbrali na eni strani, Savel in Izraelci pa na drugi. Iz filistejskih čet je prišel Goljat, ki je pozival Izraelce, naj med sabo izberejo nekoga, ki se bo bojeval z njim. Goljat predlaga, da če umre on, bodo Filistejci postali služabniki Izraelcev, če pa zmaga njegov nasprotnik, pa obratno. Savel in vsi Izraelci so se tega velikana bali. David, čigar trije starejši bratje so bili v Savlovi vojski, pa je po naročilu svojega očeta nesel bratom hrano v tabor. Prišel je ravno pravi čas, da je slišal Goljatovo izzivanje. Kralja Savla prepriča, da se bo on bojeval z njim. Slednji mu nadane vojaško opremo, katero je David slekel, saj ni bil navajen hoditi v taki opremi. Vzel je svojo palico in izbral iz potoka pet kamnov, jih spravil v svojo pastirsko torbo in s pračo v roki šel proti Filistejcu. Ko sta se nasprotnika približevala, je David iz svoje torbe vzel kamen in ga s pračo zalučal Filistejcu v čelo. S svojo iznajdljivostjo Goljata kljub njegovi očitni premoči, saj je bil David le pastir in nevešč v bojevanju, podre na tla in premaga. Stekel je k njemu, mu vzel meč in mu z njim odsekal glavo, s čimer ga je dokončno ubil. Tako je postal izraelski junak. Kasneje v zgodbi se na dvoru spoprijatelji s Savlovim sinom, ki ga tudi prizna kot zakonitega kralja. Savel potem poskuša ubiti Davida in David pobegne v puščavo. Pridruži se Filistejcem, a še naprej na skrivaj brani svoje ljudstvo, dokler Savel in Jonatan ne umreta.</p>
