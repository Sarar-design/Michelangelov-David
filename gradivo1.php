                <h4>Uprizoritev junaka</h4>
                <p>Poleg golote, drže in mišičastega telesa je povezava z antiko tudi velikost kipa. Ta je trikrat večji od realne velikosti človeka. Visok je kar 5,17 metra (Di Cagno 24). Take kolosalne skulpture so bile značilne za antiko. Michelangelovega Davida bi lahko hitro zamenjali za upodobitev kakega drugega junaka, če ne bi samo ime kipa napotovalo na svetopisemsko figuro. Lahko bi si predstavljali, da kip, bolj kot Davida, ponazarja Herkula. Kip ima malo atributov, po katerih bi ga lahko zagotovo identificirali. Kljub velikim rokam, ki pritegnejo gledalčev pogled, je »orožje« skrito pred očmi. V njem bi lahko videli kateregakoli junaka ali pa navadnega človeka (Paoletti in Radke 389).</p>
